set1 = {1, 2, 3, 4} 

set2 = {3, 4, 5, 6} 

print("Union:", set1 | set2) # displaying the union of set1 and set2

print("Intersection:", set1 & set2) # displaying the intersection of set1 and set2

print("Difference:", set1 - set2) # displaying the difference of set1 and set2

print("Symmetric Difference:", set1 ^ set2) # displaying the symmetric difference of set1 and set2